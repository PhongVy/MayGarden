    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession session = request.getSession(false); // Không tạo session mới nếu không tồn tại

        if (session != null) {
            String userid = request.getParameter("Userid");
            String username = request.getParameter("Username");
            String password = request.getParameter("Password");
            String fullname = request.getParameter("Fullname");
            String address = request.getParameter("Address");
            String phone = request.getParameter("Phone");
            String email = request.getParameter("Email");
            String isadminString = request.getParameter("isAdmin");
            boolean isadmin = Boolean.valueOf(isadminString);
            ProfileDAO dao = new ProfileDAO();
            dao.updateProfile(userid, username, password, fullname, address, phone, email, isadmin);

            response.sendRedirect("ProfileAccount"); // Chuyển hướng sau khi cập nhật
        } else {
            // Xử lý khi session không tồn tại
            response.sendRedirect("login.jsp");
        }
    }

